	DATEDIFF(YEAR, '1/30/2000', '1/30/2001'),
	DATEDIFF(MONTH, '1/30/2000', '1/30/2001'),
	DATEDIFF(MONTH, '8/27/1998', '2/16/1999') AS [ELDER THAN],
	DATEDIFF(HOUR, '10:45:00', '11:45:00'),
	DATEDIFF(HOUR, '8/27/1998 10:45:00', '2/16/1999 11:45:00'),
	DATEDIFF(DAY, '8/27/1998 10:45:00', '2/16/1999 11:45:00')